# ── Faithfulness scoring using cosine similarity ──────────────────────────────
# Since FaithfulnessEvaluator requires an LLM, we implement semantic faithfulness:
# score = mean cosine similarity between query embedding and retrieved chunk embeddings.
# Score close to 1.0 = highly faithful retrieval; lower = less relevant chunks returned.

from sentence_transformers import SentenceTransformer, util as st_util
import torch

faithfulness_model = SentenceTransformer('all-MiniLM-L6-v2')

def semantic_faithfulness_score(query: str, retrieved_texts: list) -> float:
    """
    Measures how semantically aligned retrieved chunks are to the query.
    Returns mean cosine similarity (0–1). Higher = more faithful retrieval.
    """
    if not retrieved_texts:
        return 0.0
    q_emb  = faithfulness_model.encode(query, convert_to_tensor=True)
    c_embs = faithfulness_model.encode(retrieved_texts, convert_to_tensor=True)
    scores = st_util.cos_sim(q_emb, c_embs)[0]
    return round(float(scores.mean()), 4)

def precision_at_k(retrieved_titles: list, relevant_keyword: str) -> float:
    """
    Simple keyword-based precision: fraction of retrieved docs containing the keyword.
    Used as a proxy for precision@k when ground-truth labels are unavailable.
    """
    hits = sum(1 for t in retrieved_titles if relevant_keyword.lower() in t.lower())
    return round(hits / max(len(retrieved_titles), 1), 3)

