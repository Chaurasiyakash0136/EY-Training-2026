from enum import Enum
import uuid as _uuid

class ReviewDecision(Enum):
    APPROVE  = "approve"
    REJECT   = "reject"
    ESCALATE = "escalate"
    PENDING  = "pending"

@dataclass
class LoanRequest:
    application_id:   str
    loan_amount:      float
    applicant_summary: str
    ai_recommendation: str
    risk_score:       float
    review_required:  bool               = False
    human_decision:   Optional[ReviewDecision] = None
    reviewer_id:      Optional[str]      = None
    review_notes:     Optional[str]      = None
    review_timestamp: Optional[str]      = None
    hitl_latency_ms:  float              = 0.0

# ── LangGraph-inspired state machine ─────────────────────────────────────────
# Nodes: route → [auto_process | human_review] → finalize
# Edges carry the LoanRequest state object (mirrors LangGraph StateGraph pattern)

class HITLWorkflow:
    """
    Human-in-the-loop workflow inspired by LangGraph StateGraph.
    State transitions:
      START → route_node → auto_process_node   → END  (low-value / low-risk)
                         → human_review_node   → finalize_node → END
    """
    HIGH_VALUE_THRESHOLD = 1_000_000   # INR 10 lakhs
    HIGH_RISK_SCORE      = 0.70
    _review_queue: List[LoanRequest] = []
    _completed:    List[LoanRequest] = []

    # ── Node 1: Route ──────────────────────────────────────────────────────────
    def route_node(self, request: LoanRequest) -> str:
        """
        LangGraph routing edge — returns the name of the next node.
        Criteria:  loan > 10L  OR  risk score >= 0.70
        """
        needs_review = (
            request.loan_amount >= self.HIGH_VALUE_THRESHOLD or
            request.risk_score  >= self.HIGH_RISK_SCORE
        )
        if needs_review:
            request.review_required = True
            request.human_decision  = ReviewDecision.PENDING
            self._review_queue.append(request)
            return 'human_review_node'
        return 'auto_process_node'

    # ── Node 2a: Auto-process ──────────────────────────────────────────────────
    def auto_process_node(self, request: LoanRequest) -> LoanRequest:
        """Low-risk path — AI recommendation stands, no human needed."""
        request.human_decision  = ReviewDecision.APPROVE
        request.reviewer_id     = 'AI_AUTO'
        request.review_notes    = 'Auto-approved: below value and risk thresholds.'
        request.review_timestamp = datetime.utcnow().isoformat()
        self._completed.append(request)
        return request

    # ── Node 2b: Human review ──────────────────────────────────────────────────
    def human_review_node(self, request: LoanRequest,
                          decision: ReviewDecision,
                          reviewer: str,
                          notes: str = '') -> LoanRequest:
        """
        Simulates a human reviewer approving/rejecting/escalating.
        In production this would integrate with a task queue (Celery, SQS).
        """
        t0 = time.time()
        request.human_decision   = decision
        request.reviewer_id      = reviewer
        request.review_notes     = notes or f'Manual review by {reviewer}'
        request.review_timestamp = datetime.utcnow().isoformat()
        request.hitl_latency_ms  = round((time.time() - t0) * 1000, 2)
        if request in self._review_queue:
            self._review_queue.remove(request)
        return request

    # ── Node 3: Finalize ──────────────────────────────────────────────────────
    def finalize_node(self, request: LoanRequest) -> dict:
        """Terminal node — returns the final state dict."""
        self._completed.append(request)
        return {
            'application_id': request.application_id,
            'decision':       request.human_decision.value,
            'reviewer':       request.reviewer_id,
            'notes':          request.review_notes,
            'timestamp':      request.review_timestamp,
            'hitl_latency_ms': request.hitl_latency_ms,
        }

    # ── Graph runner (mimics LangGraph .invoke()) ──────────────────────────────
    def invoke(self, request: LoanRequest,
               simulated_reviewer: str = 'UNDERWRITER_001') -> dict:
        """
        Run the full state machine for a single LoanRequest.
        Returns the finalized state dict.
        """
        next_node = self.route_node(request)
        if next_node == 'auto_process_node':
            self.auto_process_node(request)
        else:
            # Simulate human decision: approve if risk < 0.70, else reject
            decision = (ReviewDecision.APPROVE
                        if request.risk_score < self.HIGH_RISK_SCORE
                        else ReviewDecision.REJECT)
            self.human_review_node(request, decision, simulated_reviewer,
                                   notes='Simulated manual review complete.')
        return self.finalize_node(request)

    def queue_status(self):
        print(f"\n📋 REVIEW QUEUE : {len(self._review_queue)} pending")
        for r in self._review_queue:
            print(f"   [{r.application_id}] INR {r.loan_amount:>12,.0f} | Risk: {r.risk_score:.2f} | {r.applicant_summary[:50]}")

    def completed_summary(self) -> pd.DataFrame:
        return pd.DataFrame([{
            'application_id':  r.application_id,
            'loan_amount':     r.loan_amount,
            'risk_score':      r.risk_score,
            'review_required': r.review_required,
            'decision':        r.human_decision.value if r.human_decision else None,
            'reviewer':        r.reviewer_id,
            'hitl_latency_ms': r.hitl_latency_ms,
        } for r in self._completed])


