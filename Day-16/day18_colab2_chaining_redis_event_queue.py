# ── Capstone Step 1: Redis Short-Term Memory ──────────────────────────────
# Stores last N conversation turns per session in a Redis List.
# TTL = 5 min so stale sessions auto-expire.
import json, time

SESSION_TTL = 300  # seconds before an idle session expires

def session_key(session_id):
    return f"session:{session_id}"

def save_to_memory(session_id, role, content):
    """Push one message (role + content) to the session list."""
    key = session_key(session_id)
    r.rpush(key, json.dumps({"role": role, "content": content, "ts": time.time()}))
    r.expire(key, SESSION_TTL)   # refresh TTL on every write

def get_memory(session_id, last_n=6):
    """Retrieve the last N messages for this session."""
    raw = r.lrange(session_key(session_id), -last_n, -1)
    return [json.loads(m) for m in raw]

def clear_memory(session_id):
    r.delete(session_key(session_id))

def memory_to_messages(session_id):
    """Convert stored memory into the messages format Anthropic API expects."""
    return [{"role": m["role"], "content": m["content"]} for m in get_memory(session_id)]

