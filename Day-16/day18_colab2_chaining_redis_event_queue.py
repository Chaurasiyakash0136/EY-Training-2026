# ── Capstone Step 2: TinyVectorStore — Long-Term Knowledge Recall ──────────
# Bag-of-words cosine similarity. No model download, no internet needed.
# Stores product info + policy docs; the agent queries it before answering.
import numpy as np

class TinyVectorStore:
    """Lightweight in-memory vector store using BoW cosine similarity."""
    def __init__(self):
        self.docs  = []
        self.vocab = {}

    def _embed(self, text):
        tokens = text.lower().split()
        for t in tokens:
            if t not in self.vocab:
                self.vocab[t] = len(self.vocab)
        vec = np.zeros(len(self.vocab))
        for t in tokens:
            if t in self.vocab:
                vec[self.vocab[t]] += 1
        n = np.linalg.norm(vec)
        return vec / n if n > 0 else vec

    def _pad(self, v):
        p = np.zeros(len(self.vocab))
        p[:len(v)] = v
        return p

    def add(self, doc_id, text, meta=None):
        self.docs.append({"id": doc_id, "text": text,
                          "meta": meta or {}, "vec": self._embed(text)})

    def query(self, query_text, n=2):
        qv = self._pad(self._embed(query_text))
        scored = [(float(np.dot(qv, self._pad(d["vec"]))), d) for d in self.docs]
        scored.sort(key=lambda x: -x[0])
        return [{"text": d["text"], "meta": d["meta"], "score": round(s, 3)}
                for s, d in scored[:n]]

