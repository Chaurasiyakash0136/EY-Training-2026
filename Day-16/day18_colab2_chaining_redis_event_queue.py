# ── Capstone Step 3: Tracing + Retry Infrastructure ───────────────────────
import functools, time

trace_log = []   # filled automatically as tools are called

def traced(fn):
    """Decorator: records tool name, kwargs, duration (ms), and success flag."""
    @functools.wraps(fn)
    def wrapper(*args, **kwargs):
        t0 = time.perf_counter()
        try:
            out = fn(*args, **kwargs)
            ok  = not (isinstance(out, dict) and 'error' in out)
        except Exception as e:
            out = {'error': repr(e)}
            ok  = False
        ms = round((time.perf_counter() - t0) * 1000, 2)
        trace_log.append({"tool": fn.__name__, "args": kwargs, "ms": ms, "ok": ok})
        return out
    return wrapper

def with_retry(fn, max_retries=2, delay=0.1):
    """Wrap a function with exponential-backoff retry on error-dict responses."""
    @functools.wraps(fn)
    def wrapper(*args, **kwargs):
        for attempt in range(max_retries + 1):
            res = fn(*args, **kwargs)
            if not (isinstance(res, dict) and 'error' in res):
                return res
            if attempt < max_retries:
                time.sleep(delay * (2 ** attempt))
        return res
    return wrapper

def print_trace_table():
    """Pretty-print the per-tool trace for the last agent turn."""
    if not trace_log:
        print("  (no traces yet)"); return
    print(f"\n  {'Tool':<26} {'Args':<34} {'ms':>6}  Status")
    print("  " + "─" * 74)
    for t in trace_log:
        status = "✓ OK" if t['ok'] else "✗ ERR"
        print(f"  {t['tool']:<26} {str(t['args'])[:32]:<34} {t['ms']:>6.1f}  {status}")

