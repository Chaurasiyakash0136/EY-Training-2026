def support_agent(user_text, session_id="default", max_steps=10, verbose=True):
    """
    Full orchestrating support agent.
    - Loads short-term memory for session context
    - Chains tools based on ROUTING RULES
    - Saves reply back to session memory
    - Prints trace table after each turn
    """
    trace_log.clear()

    # ── OFFLINE MOCK (no API key) ──
    if not LIVE:
        if verbose:
            print("  [mock] Running offline chain: search → order → approval gate → confirm")
        kb, _ = run_tool_cs('search_knowledge_base', {'query': 'MON-4 monitor stock price'})
        od, _ = run_tool_cs('create_order',          {'sku': 'MON-4', 'qty': 1})
        if od.get('status') == 'needs_approval':
            if verbose: print(f"  [mock] Order ${od['total']} > threshold → human approval gate triggered")
            ap, _ = run_tool_cs('approve_order', {'order_id': od['order_id']})
            if verbose: print(f"  [mock] Human approved order #{od['order_id']}")
        cf, _ = run_tool_cs('send_confirmation', {'to': 'customer@x.com', 'order_id': od['order_id']})
        run_worker()
        st, _ = run_tool_cs('check_job', {'job_id': cf['job_id']})
        save_to_memory(session_id, "user", user_text)
        reply = (
            f"(mock) KB found: {kb['results'][0]['text'][:50]}…\n"
            f"Order #{od['order_id']} — Total: ${od['total']} — "
            f"Status: approved. Confirmation email: {st['status']}."
        )
        save_to_memory(session_id, "assistant", reply)
        print_trace_table()
        return reply

    # ── LIVE MODE ──
    from anthropic import Anthropic
    A = Anthropic()

    history = memory_to_messages(session_id)
    history.append({"role": "user", "content": user_text})
    save_to_memory(session_id, "user", user_text)

    token_report = {}

    for step in range(max_steps):
        resp = A.messages.create(
            model=MODEL,
            max_tokens=1024,
            system=CAPSTONE_SYSTEM,
            tools=CAPSTONE_TOOLS,
            messages=history,
        )
        if hasattr(resp, 'usage'):
            token_report = {
                'input_tokens':          resp.usage.input_tokens,
                'cache_creation_tokens': getattr(resp.usage, 'cache_creation_input_tokens', 0),
                'cache_read_tokens':     getattr(resp.usage, 'cache_read_input_tokens', 0),
                'output_tokens':         resp.usage.output_tokens,
            }

        if resp.stop_reason == 'tool_use':
            history.append({"role": "assistant",
                             "content": [b.model_dump() for b in resp.content]})
            results = []
            for b in resp.content:
                if b.type == 'tool_use':
                    if verbose:
                        print(f"  → {b.name}({b.input})")
                    out, is_err = run_tool_cs(b.name, b.input)
                    results.append({'type': 'tool_result', 'tool_use_id': b.id,
                                    'content': json.dumps(out), 'is_error': is_err})
            history.append({"role": "user", "content": results})
            run_worker()
            continue

        reply = ''.join(b.text for b in resp.content if b.type == 'text')
        save_to_memory(session_id, "assistant", reply)
        print_trace_table()
        if token_report:
            print(f"\n  Token report (prompt caching): {token_report}")
        return reply

    return '(max steps reached)'

