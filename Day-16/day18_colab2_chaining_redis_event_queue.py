# ── 4. create_order (SYNC, with human-approval gate) ──
@traced
def create_order(sku: str, qty: int):
    """Create an order. Returns needs_approval if total > APPROVAL_THRESHOLD."""
    row = db.execute(
        'SELECT qty, price FROM inventory WHERE sku=? LIMIT 1', (sku,)
    ).fetchone()
    if not row:
        return {'error': f'Unknown SKU: {sku}'}
    have, price = row
    if qty <= 0:
        return {'error': 'Quantity must be positive'}
    if have < qty:
        return {'error': f'Insufficient stock: have {have}, need {qty}'}
    total = round(price * qty, 2)
    if total > APPROVAL_THRESHOLD:
        cur = db.execute(
            'INSERT INTO orders (sku, qty, total, status) VALUES (?, ?, ?, ?)',
            (sku, qty, total, 'needs_approval')
        )
        db.commit()
        return {
            'order_id': cur.lastrowid, 'sku': sku, 'qty': qty,
            'total': total, 'status': 'needs_approval',
            'message': (f'Order total ${total} exceeds ${APPROVAL_THRESHOLD} threshold. '
                        f'Human approval required before fulfilment.')
        }
    db.execute('UPDATE inventory SET qty = qty - ? WHERE sku = ?', (qty, sku))
    cur = db.execute(
        'INSERT INTO orders (sku, qty, total, status) VALUES (?, ?, ?, ?)',
        (sku, qty, total, 'created')
    )
    db.commit()
    return {'order_id': cur.lastrowid, 'sku': sku, 'qty': qty,
            'total': total, 'status': 'created'}

# ── 5. approve_order (SYNC — human-in-the-loop gate) ──
@traced
def approve_order(order_id: int):
    """Approve a pending high-value order. Reserves inventory on approval."""
    row = db.execute(
        'SELECT status, sku, qty FROM orders WHERE id = ?', (order_id,)
    ).fetchone()
    if not row:
        return {'error': f'Order {order_id} not found'}
    if row[0] != 'needs_approval':
        return {'error': f'Order {order_id} status is "{row[0]}", not needs_approval'}
    db.execute('UPDATE inventory SET qty = qty - ? WHERE sku = ?', (row[2], row[1]))
    db.execute("UPDATE orders SET status = 'created' WHERE id = ?", (order_id,))
    db.commit()
    return {'order_id': order_id, 'status': 'approved',
            'message': 'Order approved by human agent. Inventory reserved.'}

