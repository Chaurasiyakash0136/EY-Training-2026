# EXTENSION TASK 4 — Guarded Writes / PII Rejection
# The remember_fact tool now rejects values that look like
# email addresses or credit card numbers (15-16 digit sequences).
# It returns is_error=True with a clear explanation so the agent
# can relay the refusal to the user and recover gracefully —
# the loop never crashes, it just handles the error result.
