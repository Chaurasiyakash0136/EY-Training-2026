# ============================================================
# EXTENSION TASK 5 — Token Accounting
# After every Claude API call inside the agent loop, resp.usage
# provides input_tokens and output_tokens. This version of the
# agent loop accumulates those counts across all steps in a
# turn and prints a per-step table plus a cumulative total with
# an estimated USD cost based on claude-sonnet-4-6 pricing.
# ============================================================

# Pricing per million tokens for claude-sonnet-4-6 (mid-2025 rates)
PRICE_INPUT_PER_M  = 3.00   # USD per 1M input tokens
PRICE_OUTPUT_PER_M = 15.00  # USD per 1M output tokens

def estimate_cost(input_tokens: int, output_tokens: int) -> float:
    return (input_tokens  / 1_000_000 * PRICE_INPUT_PER_M +
            output_tokens / 1_000_000 * PRICE_OUTPUT_PER_M)


def agent_turn_with_tokens(user_text: str, max_steps: int = 6, verbose: bool = True):
    """
    Identical to agent_turn() but instruments every API call with token
    reporting. Prints a per-step row and a cumulative total at the end.
    """
    mem.append_turn("user", user_text)
    messages = mem.load_history()

    if not LIVE:
        print("(OFFLINE mode — no real API calls, no token data)")
        out, _ = run_tool("get_order", {"order_id": "A1001"})
        reply = f"(mock) Order A1001 is {out.get('status', '?')}."
        mem.append_turn("assistant", reply)
        return reply

    from anthropic import Anthropic
    clientA = Anthropic()

    total_input = total_output = 0
    print(f"\n{'Step':<6} {'In tokens':>10} {'Out tokens':>11} {'Step cost $':>12}")
    print("-" * 44)

    for step in range(max_steps):
        resp = clientA.messages.create(
            model=MODEL, max_tokens=1024, system=SYSTEM,
            tools=TOOLS, messages=messages,
        )

        # --- Per-step token accounting ---
        in_tok  = resp.usage.input_tokens
        out_tok = resp.usage.output_tokens
        total_input  += in_tok
        total_output += out_tok
        step_cost = estimate_cost(in_tok, out_tok)
        print(f"{step + 1:<6} {in_tok:>10,} {out_tok:>11,} {step_cost:>12.5f}")

        if resp.stop_reason == "tool_use":
            messages.append({
                "role": "assistant",
                "content": [b.model_dump() for b in resp.content]
            })
            results = []
            for block in resp.content:
                if block.type == "tool_use":
                    if verbose:
                        print(f"  → tool: {block.name}({block.input})")
                    out, is_err = run_tool(block.name, block.input)
                    results.append({
                        "type":        "tool_result",
                        "tool_use_id": block.id,
                        "content":     json.dumps(out),
                        "is_error":    is_err,
                    })
            messages.append({"role": "user", "content": results})
            continue

        # end_turn — extract reply, save to Redis, print summary
        text = "".join(b.text for b in resp.content if b.type == "text")
        mem.append_turn("assistant", text)

        total_cost = estimate_cost(total_input, total_output)
        print("-" * 44)
        print(f"{'TOTAL':<6} {total_input:>10,} {total_output:>11,} {total_cost:>12.5f}")
        print(f"\nAgent: {text}")
        return text

    return "(stopped: max steps reached)"


