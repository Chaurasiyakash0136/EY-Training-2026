# ============================================================
# EXTENSION TASK 1 — Rolling Summary / Compaction
# When conversation history exceeds COMPACTION_THRESHOLD turns,
# the oldest turns are summarised into one synthetic assistant
# message using a cheap Claude call. The recent KEEP_RECENT
# turns are preserved untouched. This keeps the Redis list and
# the context window bounded so long sessions never blow up.
# ============================================================

import json
from anthropic import Anthropic

COMPACTION_THRESHOLD = 6   # trigger compaction when history exceeds this
KEEP_RECENT          = 2   # always preserve this many most-recent turns

def compact_history_if_needed(mem_arg, verbose=True):
    """
    Checks if history exceeds COMPACTION_THRESHOLD.
    If so: summarises the oldest turns via Claude, replaces them
    with one synthetic assistant summary message, and rewrites
    the Redis list. Returns True if compaction ran, False otherwise.
    """
    history = mem_arg.load_history()
    if len(history) <= COMPACTION_THRESHOLD:
        if verbose:
            print(f"History has {len(history)} turns — no compaction needed.")
        return False

    old_turns    = history[:-KEEP_RECENT]   # turns to compress
    recent_turns = history[-KEEP_RECENT:]   # turns to keep intact

    # Build a plain-text transcript of the old turns for the summariser
    transcript = "\n".join(
        f"{t['role'].upper()}: "
        + (t['content'] if isinstance(t['content'], str)
           else json.dumps(t['content']))
        for t in old_turns
    )

    if LIVE:
        clientA = Anthropic()
        summary_resp = clientA.messages.create(
            model=MODEL,
            max_tokens=256,
            messages=[{
                "role": "user",
                "content": (
                    "Summarise the following conversation excerpt in 2-3 sentences, "
                    "preserving any key facts (orders, preferences, names):\n\n"
                    + transcript
                )
            }]
        )
        summary_text = summary_resp.content[0].text
    else:
        summary_text = (
            "(mock summary) User introduced themselves, asked about orders "
            "A1001 and A1003, and set their shipping preference to express."
        )

    # Compacted history = 1 summary turn + the KEEP_RECENT most recent turns
    compacted = [{
        "role": "assistant",
        "content": f"[Summary of earlier conversation]: {summary_text}"
    }]
    compacted += recent_turns

    # Overwrite the Redis list with the compacted version
    mem_arg.r.delete(mem_arg.h_key)
    for turn in compacted:
        mem_arg.append_turn(turn["role"], turn["content"])

    if verbose:
        print(f"Compacted {len(old_turns)} old turns → 1 summary message.")
        print(f"History now has {len(mem_arg.load_history())} turns "
              f"(1 summary + {KEEP_RECENT} recent).")
        print(f"Summary text: {summary_text}")
    return True


