# ── 3B · call_claude with retries + rate-limit ──────────────
MAX_RETRIES   = 3
RETRY_BACKOFF = [1, 2, 4]           # seconds between attempts

def call_claude(prompt: str,
                model: str       = MODEL_ROUTINE,
                system: str      = "",
                max_tokens: int  = 400,
                temperature: float = 0.2) -> LLMResult:
    """
    Replaces the original call_claude().
    Adds:
      • token-bucket rate limiting (blocks if too many calls/sec)
      • exponential-backoff retries on RateLimitError / 5xx
      • retry_count attached to returned result via a closure attr
    """
    # ---- rate-limit gate ----
    acquired = RATE_LIMITER.acquire(tokens=1.0, timeout=30.0)
    if not acquired:
        raise RuntimeError("Rate limiter timeout — too many calls in flight.")

    t0          = time.perf_counter()
    last_exc    = None
    retry_count = 0

    for attempt in range(MAX_RETRIES):
        try:
            if USE_MOCK:
                # Mock path — instant, no network needed
                text = _mock_response(prompt, model)
                it   = _estimate_tokens(system + prompt)
                ot   = _estimate_tokens(text)
                stop = "end_turn"
                mock = True
            else:
                import anthropic as _anthropic
                client = _anthropic.Anthropic()
                msg    = client.messages.create(
                    model=model, max_tokens=max_tokens,
                    temperature=temperature,
                    system=system or "You are a helpful assistant.",
                    messages=[{"role": "user", "content": prompt}],
                )
                text = "".join(b.text for b in msg.content
                               if getattr(b, "type", "") == "text")
                it, ot = msg.usage.input_tokens, msg.usage.output_tokens
                stop   = msg.stop_reason
                mock   = False

            # ---- success path ----
            dt    = (time.perf_counter() - t0) * 1000
            price = PRICES.get(model, {"in": 0, "out": 0})
            cost  = it / 1e6 * price["in"] + ot / 1e6 * price["out"]
            result = LLMResult(text, model, it, ot, stop,
                               round(dt, 1), round(cost, 6), mock)
            result.retry_count = retry_count      # attach for telemetry
            return result

        except Exception as exc:
            # Decide whether to retry
            retryable = False
            if not USE_MOCK:
                import anthropic as _anthropic
                retryable = isinstance(
                    exc,
                    (_anthropic.RateLimitError, _anthropic.APIStatusError)
                )
            last_exc    = exc
            retry_count = attempt + 1

            if retryable and attempt < MAX_RETRIES - 1:
                wait = RETRY_BACKOFF[attempt]
                log_event("llm.retry", level="WARN", model=model,
                          attempt=attempt + 1, wait_s=wait,
                          error=str(exc)[:80])
                time.sleep(wait)
            else:
                raise   # non-retryable or exhausted retries

    raise last_exc  # should never reach here


