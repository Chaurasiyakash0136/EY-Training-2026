# ── 6C · Load audit log back from disk ──────────────────────
def load_audit_log(path: pathlib.Path = AUDIT_LOG_PATH) -> list[dict]:
    """
    Reads the .jsonl file line-by-line and returns a list of dicts.
    Skips blank lines safely.
    """
    if not path.exists():
        print(f"[load_audit_log] File not found: {path}")
        return []
    records = []
    with path.open("r", encoding="utf-8") as fh:
        for lineno, line in enumerate(fh, start=1):
            line = line.strip()
            if not line:
                continue
            try:
                records.append(json.loads(line))
            except json.JSONDecodeError as e:
                print(f"[load_audit_log] Skipping corrupt line {lineno}: {e}")
    return records


# ── 6D · Reload & re-verify the hash chain ──────────────────
def reload_and_verify(path: pathlib.Path = AUDIT_LOG_PATH) -> bool:
    """
    Loads the persisted log from disk and runs verify_chain() on it.
    This proves the file hasn't been tampered with between sessions.
    Prints a clear pass / fail report.
    """
    global AUDIT_LOG          # ← declared FIRST before any use

    print(f"\n{'='*44}")
    print(" AUDIT LOG RELOAD & VERIFICATION")
    print(f"{'='*44}")

    reloaded = load_audit_log(path)
    print(f" file             : {path}")
    print(f" records on disk  : {len(reloaded)}")
    print(f" records in memory: {len(AUDIT_LOG)}")

    if not reloaded:
        print(" result           : ⚠️  NO RECORDS FOUND")
        print(f"{'='*44}\n")
        return False

    # Temporarily swap AUDIT_LOG to verify the disk version
    original   = AUDIT_LOG
    AUDIT_LOG  = reloaded
    valid      = verify_chain()
    AUDIT_LOG  = original          # restore in-memory list

    status = "✅  CHAIN VALID — no tampering detected" if valid \
             else "❌  CHAIN BROKEN — tampering detected!"
    print(f" chain status     : {status}")

    if reloaded:
        print(f" first record ts  : {reloaded[0]['ts']}")
        print(f" last  record ts  : {reloaded[-1]['ts']}")
    print(f"{'='*44}\n")
    return valid


