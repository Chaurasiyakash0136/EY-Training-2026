import threading

# ── 3A · Token Bucket ───────────────────────────────────────
class TokenBucket:
    """
    Thread-safe token-bucket rate limiter.
    capacity  : max tokens in the bucket (burst headroom)
    rate      : tokens refilled per second
    Each call_claude() call costs 1 token.
    """
    def __init__(self, capacity: float = 10, rate: float = 2.0):
        self.capacity   = capacity
        self.rate       = rate          # tokens / second
        self._tokens    = capacity
        self._last_refill = time.monotonic()
        self._lock      = threading.Lock()

    def _refill(self):
        now     = time.monotonic()
        elapsed = now - self._last_refill
        added   = elapsed * self.rate
        self._tokens    = min(self.capacity, self._tokens + added)
        self._last_refill = now

    def acquire(self, tokens: float = 1.0, timeout: float = 30.0) -> bool:
        """
        Block until `tokens` are available (up to `timeout` seconds).
        Returns True if acquired, False if timed-out.
        """
        deadline = time.monotonic() + timeout
        while True:
            with self._lock:
                self._refill()
                if self._tokens >= tokens:
                    self._tokens -= tokens
                    return True
            wait = tokens / self.rate          # estimated wait
            if time.monotonic() + wait > deadline:
                return False
            time.sleep(min(wait, 0.5))

# One shared limiter for the whole notebook
