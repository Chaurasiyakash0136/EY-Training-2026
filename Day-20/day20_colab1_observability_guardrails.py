# ── 6A · Save a single record (append-only) ─────────────────
def save_audit_record(record: dict, path: pathlib.Path = AUDIT_LOG_PATH) -> None:
    """
    Appends one audit record as a JSON line to the file.
    Opens in 'a' mode — never overwrites existing records.
    This is the WORM (Write Once Read Many) guarantee on disk.
    """
    with path.open("a", encoding="utf-8") as fh:
        fh.write(json.dumps(record, sort_keys=True) + "\n")


