"""## 5 · Input guardrails

Before a single token reaches the model, validate what is going in:

- **Schema / required fields** — refuse a lead with no lawful `consent_basis`
- **Prompt-injection heuristic** — flag text that tries to hijack instructions (lead L-003!)
- **Topic scope** — keep the agent on task

A guardrail returns a *decision*, not just a boolean — so the reason is auditable.
"""

@dataclass
class GuardResult:
    allowed: bool
    rule: str
    reason: str = ""
    severity: str = "low"

REQUIRED_LEAD_FIELDS = {"lead_id", "company", "industry"}

INJECTION_PATTERNS = [
    r"ignore (all |previous |prior )?instructions",
    r"disregard (the )?(above|previous)",
    r"system prompt", r"you are now", r"email everyone", r"reveal your",
]

def gr_required_fields(lead: dict) -> GuardResult:
    missing = REQUIRED_LEAD_FIELDS - set(lead)
    if missing:
        return GuardResult(False, "required_fields", f"missing {sorted(missing)}", "high")
    return GuardResult(True, "required_fields")

def gr_prompt_injection(text: str) -> GuardResult:
    low = (text or "").lower()
    for pat in INJECTION_PATTERNS:
        if re.search(pat, low):
            return GuardResult(False, "prompt_injection", f"matched /{pat}/", "high")
    return GuardResult(True, "prompt_injection")

def gr_topic_scope(text: str, allowed=("lead", "company", "sales", "outreach", "industry")) -> GuardResult:
    # toy heuristic: very long inputs with none of the expected terms are suspicious
    low = (text or "").lower()
    if len(low) > 20 and not any(a in low for a in allowed):
        return GuardResult(True, "topic_scope", "no expected terms (warn only)", "low")
    return GuardResult(True, "topic_scope")

def check_input(lead: dict) -> list[GuardResult]:
    results = [gr_required_fields(lead),
               gr_prompt_injection(lead.get("notes", "")),
               gr_topic_scope(lead.get("notes", ""))]
    for g in results:
        if not g.allowed:
            log_event("guardrail.block", level="WARN", lead_id=lead.get("lead_id"),
                      rule=g.rule, reason=g.reason, severity=g.severity)
    return results

for lead in SYNTHETIC_LEADS:
    res = check_input(lead)
    blocked = [g.rule for g in res if not g.allowed]
    print(lead["lead_id"], "-> blocked:", blocked or "none")

"""## 6 · Output guardrails — PII detection & redaction

Inputs are only half the risk; the **model's output** (and anything you log) can leak PII.
We detect emails/phones, replace them with **typed tokens** (`<EMAIL_1>`), and keep a sealed
re-identification map so authorised code can reverse it. Logs and audit records only ever see
the redacted form.
"""

# ============================================================
# SECTION 6 · Output Guardrails — PII, Grounding & Usefulness
# ============================================================

EMAIL_RE = re.compile(r"[\w.+-]+@[\w-]+\.[\w.-]+")
PHONE_RE = re.compile(r"\+?\d[\d\s().-]{7,}\d")

# ── PII Redaction ────────────────────────────────────────────
def redact(text: str):
    """Return (redacted_text, reidentification_map). Map is the *only* place PII survives."""
    mapping, counters = {}, {"EMAIL": 0, "PHONE": 0}
    def _sub(kind, regex, s):
        def r(m):
            counters[kind] += 1
            tok = f"<{kind}_{counters[kind]}>"
            mapping[tok] = m.group(0)
            return tok
        return regex.sub(r, s)
    out = _sub("EMAIL", EMAIL_RE, text or "")
    out = _sub("PHONE", PHONE_RE, out)
    return out, mapping

def contains_pii(text: str) -> bool:
    return bool(EMAIL_RE.search(text or "") or PHONE_RE.search(text or ""))

def gr_no_pii_in_output(text: str) -> GuardResult:
    return (GuardResult(False, "pii_in_output", "raw PII present", "high")
            if contains_pii(text) else GuardResult(True, "pii_in_output"))

def gr_valid_json(text: str) -> GuardResult:
    try:
        json.loads(text); return GuardResult(True, "valid_json")
    except Exception as e:
        return GuardResult(False, "valid_json", str(e)[:60], "medium")


# ── Groundedness Guardrail ───────────────────────────────────
def gr_grounded(summary: str, source: str) -> GuardResult:
    """
    Checks whether the summary is grounded in the source text.

    Method: token overlap ratio — what fraction of meaningful words
    in the summary also appear in the source.

    Threshold : overlap >= 0.15  → allowed (grounded)
                overlap <  0.15  → blocked (likely hallucinated)

    Why 0.15?  A summary naturally uses different words than the source
    (synonyms, rephrasing) so we use a low but non-zero floor.
    Any overlap below 15% means almost none of the summary tokens
    came from the source — a strong hallucination signal.
    """
    s   = set(re.findall(r"[a-z]{4,}", (summary or "").lower()))
    src = set(re.findall(r"[a-z]{4,}", (source  or "").lower()))

    if not s:
        return GuardResult(True, "grounded", "empty summary — nothing to check")

    overlap     = len(s & src) / len(s)
    overlap_pct = round(overlap * 100, 1)

    if overlap >= 0.15:
        return GuardResult(
            True, "grounded",
            f"overlap={overlap_pct}% (>= 15% threshold) — summary tokens "
            f"found in source; grounding looks ok",
            "low"
        )
    else:
        return GuardResult(
            False, "grounded",
            f"overlap={overlap_pct}% (< 15% threshold) — too few summary "
            f"tokens match source; possible hallucination",
            "medium"
        )


# ── Usefulness Guardrail ─────────────────────────────────────
def gr_usefulness(text: str) -> GuardResult:
    """
    Checks whether the model output is actually useful to a sales rep.

    Three failure modes caught here:

    1. TOO SHORT  — fewer than 15 words means the model gave a non-answer
                    (e.g. 'Acknowledged.' or 'No summary available.')
                    A useful summary needs enough words to convey something.

    2. VAGUE FILLER — common hedge phrases that signal the model produced
                      content but said nothing actionable. A sales rep
                      cannot act on 'as per your request' or 'noted above'.

    3. COPY-PASTE OF INSTRUCTION — if the output just repeats the prompt
                                   keywords ('summarise', 'one-line', 'sales rep')
                                   without adding new information, it failed.

    Severity is 'medium' because a low-usefulness output is bad but not
    as dangerous as PII leakage (high) or missing fields (high).
    """
    if not text or not text.strip():
        return GuardResult(
            False, "usefulness",
            "output is empty — model returned nothing",
            "high"
        )

    words = text.strip().split()
    word_count = len(words)

    # ── Check 1: too short ───────────────────────────────────
    if word_count < 15:
        return GuardResult(
            False, "usefulness",
            f"output too short ({word_count} words < 15 word minimum) — "
            f"likely a non-answer or acknowledgement, not a real summary",
            "medium"
        )

    # ── Check 2: vague filler phrases ───────────────────────
    VAGUE_PATTERNS = [
        r"as per (your )?(request|above|instructions)",
        r"noted (above|below|here)",
        r"see (above|below)",
        r"no (information|data|details) (available|provided|found)",
        r"(unable|not able) to (provide|generate|summarise|summarize)",
        r"mock response",
        r"i (cannot|can't|am unable to)",
        r"(please|kindly) (provide|clarify|specify)",
    ]
    low = text.lower()
    for pat in VAGUE_PATTERNS:
        if re.search(pat, low):
            return GuardResult(
                False, "usefulness",
                f"output contains vague filler pattern '{pat}' — "
                f"model hedged instead of producing actionable content",
                "medium"
            )

    # ── Check 3: instruction echo (copy-paste of prompt) ────
    INSTRUCTION_ECHO = [
        "summarise this lead",
        "one-line outreach",
        "for a sales rep",
        "enrich this lead",
        "one pain point",
    ]
    for phrase in INSTRUCTION_ECHO:
        if phrase in low:
            return GuardResult(
                False, "usefulness",
                f"output echoes the instruction ('{phrase}') — "
                f"model repeated the prompt instead of answering it",
                "medium"
            )

    # ── All checks passed ────────────────────────────────────
    return GuardResult(
        True, "usefulness",
        f"output has {word_count} words, no vague fillers, "
        f"no instruction echo — looks actionable",
        "low"
    )


# ── Combined output guardrail check ─────────────────────────
def check_output(text: str, summary: str = "", source: str = "") -> list[GuardResult]:
    """
    Runs all output guardrails in one call.
    Returns a list of GuardResult — callers check for any allowed=False.
    """
    results = [
        gr_no_pii_in_output(text),
        gr_usefulness(text),
    ]
    if summary and source:
        results.append(gr_grounded(summary, source))
    for g in results:
        if not g.allowed:
            log_event("guardrail.output_block", level="WARN",
                      rule=g.rule, reason=g.reason, severity=g.severity)
    return results


